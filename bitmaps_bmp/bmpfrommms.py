def hex_to_chars(input_data):
    pixels = input_data.split()
    chars = ""
    for i in range(len(pixels)):
        pixels[i] = pixels[i].strip('#')
        for j in range(0, len(pixels[0]), 2):
            next_char = chr(int(pixels[i][j:j+2], 16))
            chars += next_char

    return chars


